package com.parker.hotkey.domain.util

/**
 * 앱의 현재 상태를 나타내는 enum 클래스
 * 다양한 시나리오(앱 최초 실행, 일반 실행, 포그라운드 복귀 등)에서
 * 적절한 데이터 로드 전략을 선택하는 데 사용됩니다.
 */
enum class AppStatus {
    /**
     * 앱이 최초로 설치되어 실행되는 상태
     * 이 상태에서는 초기 설정 및 데이터 로드가 필요합니다.
     */
    FRESH_INSTALL,
    
    /**
     * 앱이 정상적으로 실행되는 상태
     * 이미 앱이 설치되어 있고, 초기 설정이 완료된 상태입니다.
     */
    NORMAL_LAUNCH,
    
    /**
     * 앱이 백그라운드에서 포그라운드로 복귀한 상태
     * 이 상태에서는 필요에 따라 데이터를 새로고침합니다.
     */
    FOREGROUND_RESUME,
    
    /**
     * 네비게이션 메뉴에서 다시 지도 화면으로 복귀한 상태
     * 이 상태에서는 로컬 데이터를 우선 표시하고 필요시에만 API를 호출합니다.
     */
    NAVIGATION_RETURN,
    
    /**
     * 앱이 업데이트된 후 처음 실행되는 상태
     * 이 상태에서는 필요한 마이그레이션 작업 등을 수행합니다.
     */
    AFTER_UPDATE
} 